arr = [int(i) for i in input().split()]
n = arr[0]
m = arr[1]

matrix = [[0 for j in range(m)] for i in range(n)]

elem = 1
for j in range(n + m - 1):
    for i in range(n):
        if j - i in range(m):
            matrix[i][j-i] = elem
            elem += 1

for i in range(n):
    for j in range(m):
        print(str(matrix[i][j]).ljust(3), end='')
    print()
