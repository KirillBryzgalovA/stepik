# —Å—É–º–º–∞ –ø–æ –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏
def sled_one(matrix, n):
    summa = 0
    for i in range(n):
        summa += int(matrix[i][i])
    return summa


# —Å—É–º–º–∞ –ø–æ  –≤—Ç–æ—Ä–∏—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏
def sled_two(matrix, n):
    summa = 0
    for i in range(n):
        summa += int(matrix[i][n - i - 1])
    return summa


n = int(input())
seq = [i for i in range(1, n ** 2 + 1)]
#flag = True

matrix = []
temp_list = []  # –≤—Ä–µ–º. —Å–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –≤—Å–µ—Ö —á–∏—Å–µ–ª –º–∞—Ç—Ä–∏—Ü—ã
for i in range(n):
    temp = [int(num) for num in input().split()]
    temp_list.extend(temp)
    matrix.append(temp)

for elem in seq:
    if elem not in list(set(temp_list)):
        print("NO")
        exit(0)

trans_matrix = [[matrix[i][j] for i in range(n)] for j in range(n)]

result = []
# –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å–ø–∏—Å–æ–∫
# –ø–æ–ª—É—á–∞–µ–º —Å—É–º–º—ã —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤
for i in range(n):
    result.append(sum(matrix[i]))
    result.append(sum(trans_matrix[i]))

a = sled_one(matrix, n)
b = sled_two(matrix, n)
c = sum(result) / len(result)
d = sum(matrix[0])

if a != b or c != d:
    print("NO")
else:
    print("YES")
