arr = input().split()

result = {}
for i in arr:
    result[i] = result.get(i, 0) + 1

for key, value in result.items():
    if value > 1:
        # –ø–æ–∏—Å–∫ –≤ –º–∞—Å—Å–∏–≤–µ
        for num in range(value):
            pos = arr.index(key)
            arr[pos] = key + "_" + str(num)

s = " ".join(arr)
s = s.replace('_0', '')
print(s)
