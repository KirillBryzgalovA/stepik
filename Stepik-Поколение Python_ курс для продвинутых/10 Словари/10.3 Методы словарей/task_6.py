sentense = input().lower()
s = sentense.replace('.', '').replace(',', '').replace(';', '').replace(':', '').replace('-', '').replace('?', '').replace('!', '').replace('(', '').replace(')', '').lower()

arr = s.split()

result = {}
for i in arr:
    result[i] = result.get(i, 0) + 1

min_res = 99999
for key, value in result.items():
    if value < min_res:
        min_res = value

arr_res = {}
for key, value in result.items():
    if value == min_res:
        arr_res[key] = value
print(min(arr_res))