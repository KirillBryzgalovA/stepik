with open('lines.txt', encoding='utf-8') as file:
    arr = [line.strip() for line in file.readlines()]
    arr_len = list(map(lambda x: len(x), arr))
    m = max(arr_len)

    print(*(filter(lambda x: len(x) == m, arr)), sep='\n')
