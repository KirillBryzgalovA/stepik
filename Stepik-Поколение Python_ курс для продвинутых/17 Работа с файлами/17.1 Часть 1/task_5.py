from functools import reduce

file = open('nums.txt', encoding='utf-8')
lines = list(map(str.strip, file.readlines()))
p = list(filter(lambda num: num.isdigit(), lines))
result = reduce(lambda x, y: int(x) + int(y), p, 0)
print(result)
file.close()
