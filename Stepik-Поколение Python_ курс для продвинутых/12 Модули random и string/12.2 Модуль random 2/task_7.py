import random

n = int(input())

arr_pupil = [input() for _ in range(n)]
arr_frend = arr_pupil[:]  # –ö–æ–ø–∏—è —Å–ø–∏—Å–∫–∞

for pupil in arr_pupil:
    while True:
        frend = random.choice(arr_frend)
        if pupil != frend:
            print(pupil, '-', frend)
            arr_frend.remove(frend)
            break
