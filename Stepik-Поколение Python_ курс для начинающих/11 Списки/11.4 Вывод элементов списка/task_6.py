arr = []
for _ in range(n):
    s = input()
    arr.append(s)

k = int(input())
arr_query = []
for _ in range(k):
    s = input()
    arr_query.append(s)

for s in arr:
    count = 0
    for query in arr_query:
        if s.lower().find(query.lower()) != -1:
            count += 1
    if count == len(arr_query):
        print(s)
