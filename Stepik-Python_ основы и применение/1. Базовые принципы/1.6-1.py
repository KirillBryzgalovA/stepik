dict_class = {'A': [], 'B': ['A'], 'C': ['B', 'D'], 'E': ['C'], 'F': ['C'], 'I': ['F'], 'H': ['F'], 'D': []}
list_query = [['A', 'E'], ['D', 'E'], ['C', 'I'], ['A', 'I'], ['A', 'H']]


def build_path(q, dict):
    parent = q[0]
    children = q[1]

    if parent in dict:
        if (parent in dict[children]) or (children == parent):
            print("Yes")
            return

        for c in dict[children]:
            if c == parent:
                print("Yes")
                return
            else:
                build_path([parent, c], dict)

        if dict[children] == []:
            print("No")
            return
    else:
        print("No")
        return


for q in list_query:
    build_path(q, dict_class)
